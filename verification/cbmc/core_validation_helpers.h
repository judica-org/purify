static inline int purify_cbmc_compare_be(const unsigned char* lhs, const unsigned char* rhs, size_t size) {
    size_t i;
    for (i = 0; i < size; ++i) {
        if (lhs[i] < rhs[i]) {
            return -1;
        }
        if (lhs[i] > rhs[i]) {
            return 1;
        }
    }
    return 0;
}

static inline void purify_cbmc_copy_bytes(unsigned char* out, const unsigned char* in, size_t size) {
    size_t i;
    for (i = 0; i < size; ++i) {
        out[i] = in[i];
    }
}

static inline int purify_cbmc_decrement_be(unsigned char* value, size_t size) {
    size_t i = size;
    while (i > 0u) {
        --i;
        if (value[i] != 0u) {
            --value[i];
            return 1;
        }
        value[i] = 0xffu;
    }
    return 0;
}

static inline int purify_cbmc_increment_be(unsigned char* value, size_t size) {
    size_t i = size;
    while (i > 0u) {
        --i;
        value[i] = (unsigned char)(value[i] + 1u);
        if (value[i] != 0u) {
            return 1;
        }
    }
    return 0;
}
